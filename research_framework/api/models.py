# Models for initiating research operations.

class ResearchRequest(BaseModel):
    """
    Request model for starting a multi-agent research investigation.

    This is the primary request model for the /research endpoint.
    It supports both simple research (hypothesis only) and complex
    investigations with custom agents and specific questions.

    Attributes:
        hypothesis: Main research hypothesis or question to investigate
        research_questions: Optional specific questions to address
        custom_agents: Optional custom agent definitions (max 5)
        max_agents: Maximum number of agents to use (1-5)
        model_tier: Model configuration tier (default, budget, premium, fast)
        include_citations: Whether to add citations to final report
        citation_style: Style for citation formatting

    Example:
        >>> request = ResearchRequest(
        ...     hypothesis="AI will transform healthcare by 2030",
        ...     custom_agents=[
        ...         AgentDefinition(name="Tech Analyst", focus="AI technology"),
        ...         AgentDefinition(name="Policy Analyst", focus="regulations")
        ...     ],
        ...     max_agents=2,
        ...     include_citations=True
        ... )
    """
    hypothesis: str = Field(
        ...,
        description="The main research hypothesis or question",
        example="Artificial intelligence will significantly improve medical diagnosis accuracy by 2030",
        min_length=10,
        max_length=1000
    )
    research_questions: Optional[List[str]] = Field(
        default=None,
        description="Specific research questions to investigate",
        example=["What AI techniques are used in medical diagnosis?"]
    )
    custom_agents: Optional[List[AgentDefinition]] = Field(
        default=None,
        description="Custom agent definitions (max 5)",
        max_length=5
    )
    max_agents: int = Field(
        default=3,
        ge=1,
        le=5,
        description="Maximum number of agents to use"
    )
    model_tier: str = Field(
        default="default",
        description="Model tier: default, budget, premium, fast"
    )
    include_citations: bool = Field(
        default=True,
        description="Whether to add citations to the final report"
    )
    citation_style: CitationStyle = Field(
        default=CitationStyle.SIMPLE,
        description="Citation style for the report"
    )

    class Config:
        """Pydantic model configuration with example."""
        json_schema_extra = {
            "example": {
                "hypothesis": "AI will transform healthcare diagnostics by 2030",
                "research_questions": [
                    "What AI techniques are currently used?",
                    "What are the barriers to adoption?"
                ],
                "custom_agents": [
                    {"name": "Tech Analyst", "focus": "technical capabilities"},
                    {"name": "Policy Analyst", "focus": "regulatory landscape"}
                ],
                "max_agents": 2,
                "model_tier": "default",
                "include_citations": True,
                "citation_style": "simple"
            }
        }


class QuickSearchRequest(BaseModel):
    """
    Request model for a quick web search operation.

    This lightweight model is used for simple search queries
    without the full research orchestration.

    Attributes:
        query: Search query string
        max_results: Maximum number of results to return (1-20)
        backend: Optional search backend override

    Example:
        >>> request = QuickSearchRequest(
        ...     query="AI in healthcare 2024",
        ...     max_results=10,
        ...     backend="tavily"
        ... )
    """
    query: str = Field(
        ...,
        description="Search query",
        example="AI in healthcare 2024",
        min_length=1,
        max_length=500
    )
    max_results: int = Field(
        default=5,
        ge=1,
        le=20,
        description="Maximum number of results to return"
    )
    backend: Optional[str] = Field(
        default=None,
        description="Search backend: tavily, brave, serper, bing, duckduckgo"
    )


class CitationRequest(BaseModel):
    """
    Request model for adding citations to content.

    This model is used to process existing content and add
    inline citations based on provided sources.

    Attributes:
        content: The text content to add citations to
        sources: List of source dictionaries with title, url, etc.
        style: Citation formatting style

    Example:
        >>> request = CitationRequest(
        ...     content="AI is transforming healthcare...",
        ...     sources=[{"title": "AI Study", "url": "https://..."}],
        ...     style=CitationStyle.APA
        ... )
    """
    content: str = Field(
        ...,
        description="Content to add citations to",
        min_length=1
    )
    sources: List[Dict[str, Any]] = Field(
        ...,
        description="Available sources for citation"
    )
    style: CitationStyle = Field(
        default=CitationStyle.SIMPLE,
        description="Citation formatting style"
    )


