# Models for web search results.

class SearchResult(BaseModel):
    """
    A single web search result.

    Represents one item from a search query response.

    Attributes:
        title: Page title
        url: Page URL
        snippet: Text excerpt from the page
        score: Relevance score (0-1, if available)
    """
    title: str = Field(..., description="Page title")
    url: str = Field(..., description="Page URL")
    snippet: str = Field(..., description="Text excerpt")
    score: Optional[float] = Field(None, description="Relevance score")


class SearchResponse(BaseModel):
    """
    Response model for web search queries.

    Contains the search results and metadata about the search.

    Attributes:
        query: Original search query
        backend: Search backend used (tavily, brave, etc.)
        results: List of search results
        result_count: Number of results returned
        answer: AI-generated answer (if available from backend)
    """
    query: str = Field(..., description="Original query")
    backend: str = Field(..., description="Backend used")
    results: List[SearchResult] = Field(..., description="Search results")
    result_count: int = Field(..., description="Number of results")
    answer: Optional[str] = Field(None, description="AI-generated answer")


