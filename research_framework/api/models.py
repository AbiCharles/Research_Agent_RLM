"""
Pydantic models for the FastAPI service.

This module defines all request and response schemas for the Research Framework API.
Pydantic models provide automatic validation, serialization, and OpenAPI schema
generation for the API endpoints.

Model Categories:
    - Enums: Status codes, roles, and configuration options
    - Agent Models: Definitions for research agents
    - Research Models: Request/response for research operations
    - Search Models: Web search request/response
    - Citation Models: Citation processing request/response
    - Health Models: API health and info responses
    - Error Models: Standardized error responses

Example Usage:
    >>> from api.models import ResearchRequest, ResearchStatus
    >>> request = ResearchRequest(
    ...     hypothesis="AI will transform healthcare",
    ...     max_agents=3
    ... )
    >>> print(request.model_dump())
"""

from datetime import datetime
from enum import Enum
from typing import List, Dict, Any, Optional
from pydantic import BaseModel, Field


# ============================================================================
# Enums
# ============================================================================
# Enums define fixed sets of values for type safety and API documentation.
# Using str as base class allows JSON serialization as strings.

class ResearchStatus(str, Enum):
    """
    Status of a research task throughout its lifecycle.

    States:
        PENDING: Task created but not yet started
        RUNNING: Task is currently being processed
        COMPLETED: Task finished successfully
        FAILED: Task encountered an error
        CANCELLED: Task was cancelled by user or system
    """
    PENDING = "pending"
    RUNNING = "running"
    COMPLETED = "completed"
    FAILED = "failed"
    CANCELLED = "cancelled"


class AgentRole(str, Enum):
    """
    Predefined agent role types in the research framework.

    Roles:
        LEAD_RESEARCHER: Orchestrates multiple agents and synthesizes findings
        RESEARCH_AGENT: Specialized investigator for specific domains
        CITATION_AGENT: Adds source citations to research content
    """
    LEAD_RESEARCHER = "lead_researcher"
    RESEARCH_AGENT = "research_agent"
    CITATION_AGENT = "citation_agent"


class CitationStyle(str, Enum):
    """
    Supported citation formatting styles.

    Styles:
        SIMPLE: Basic format - [1] Title (URL)
        APA: American Psychological Association style
        MLA: Modern Language Association style
        CHICAGO: Chicago Manual of Style
    """
    SIMPLE = "simple"
    APA = "apa"
    MLA = "mla"
    CHICAGO = "chicago"


# ============================================================================
# Agent Models
# ============================================================================
# Models for defining and reporting on research agents.

class AgentDefinition(BaseModel):
    """
    Definition for a custom research agent.

    This model allows users to define specialized agents with specific
    focus areas for their research investigation. Each agent will
    investigate aspects related to their defined focus.

    Attributes:
        name: Human-readable agent name (e.g., "Market Analyst")
        focus: Research focus area describing what the agent investigates
        tools: List of tool names the agent can use (default: web_search)

    Example:
        >>> agent = AgentDefinition(
        ...     name="Tech Expert",
        ...     focus="technical feasibility and implementation challenges"
        ... )
    """
    name: str = Field(
        ...,
        description="Agent name",
        example="Market Analyst",
        min_length=1,
        max_length=100
    )
    focus: str = Field(
        ...,
        description="Research focus area",
        example="market trends and competition",
        min_length=1,
        max_length=500
    )
    tools: Optional[List[str]] = Field(
        default=["web_search"],
        description="Tools available to the agent"
    )

    class Config:
        """Pydantic model configuration."""
        json_schema_extra = {
            "example": {
                "name": "Tech Expert",
                "focus": "technical feasibility and implementation",
                "tools": ["web_search"]
            }
        }


class AgentResultResponse(BaseModel):
    """
    Response model containing results from a single research agent.

    This model represents the output of one agent's research execution,
    including findings, sources, and execution metadata.

    Attributes:
        agent_id: Unique identifier for the agent instance
        agent_name: Human-readable agent name
        status: Execution status (completed, failed, etc.)
        content: Full text content generated by the agent
        findings: List of structured finding objects
        sources: List of source references used
        duration_seconds: Time taken for agent execution
        token_usage: Token counts for API usage tracking
    """
    agent_id: str = Field(..., description="Unique agent identifier")
    agent_name: str = Field(..., description="Agent display name")
    status: str = Field(..., description="Execution status")
    content: str = Field(..., description="Generated content")
    findings: List[Dict[str, Any]] = Field(
        default_factory=list,
        description="Structured findings list"
    )
    sources: List[Dict[str, str]] = Field(
        default_factory=list,
        description="Sources referenced"
    )
    duration_seconds: Optional[float] = Field(
        None,
        description="Execution duration in seconds"
    )
    token_usage: Dict[str, int] = Field(
        default_factory=dict,
        description="Token usage statistics"
    )


