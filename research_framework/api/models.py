# Models for API health checks and framework information.

class HealthResponse(BaseModel):
    """
    API health check response.

    Returns the health status of the API and its components.

    Attributes:
        status: Overall health status ("healthy" or "unhealthy")
        version: API version string
        timestamp: Current server timestamp
        components: Health status of individual components
    """
    status: str = Field(default="healthy", description="Overall status")
    version: str = Field(..., description="API version")
    timestamp: datetime = Field(..., description="Server timestamp")
    components: Dict[str, str] = Field(..., description="Component statuses")


class BackendStatus(BaseModel):
    """
    Status of a search backend.

    Indicates whether a search backend is available and configured.

    Attributes:
        name: Backend name (tavily, brave, etc.)
        available: Whether the backend can be used
        configured: Whether API key is configured
    """
    name: str = Field(..., description="Backend name")
    available: bool = Field(..., description="Is available")
    configured: bool = Field(..., description="Is configured")


class FrameworkInfoResponse(BaseModel):
    """
    Framework information and capabilities response.

    Provides comprehensive information about the framework's
    capabilities and current configuration.

    Attributes:
        name: Framework name
        version: Framework version
        description: Brief description
        available_backends: Search backend availability
        model_tiers: Available model configuration tiers
        max_agents: Maximum agents per research task
        features: List of framework features
    """
    name: str = Field(
        default="Multi-Agent Research Framework",
        description="Framework name"
    )
    version: str = Field(..., description="Version string")
    description: str = Field(..., description="Framework description")
    available_backends: List[BackendStatus] = Field(
        ...,
        description="Search backend status"
    )
    model_tiers: List[str] = Field(..., description="Available model tiers")
    max_agents: int = Field(..., description="Max agents per task")
    features: List[str] = Field(..., description="Framework features")


