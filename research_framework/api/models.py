# Models for research operation results.

class ResearchPlanResponse(BaseModel):
    """
    Response model containing the research plan created by Lead Researcher.

    This model represents the strategic plan created before
    research execution begins.

    Attributes:
        hypothesis: The original research hypothesis
        complexity: Assessed complexity level (simple, moderate, complex)
        domains: Identified knowledge domains to investigate
        research_questions: Questions to be investigated
        agent_assignments: How work is distributed among agents
        strategy_notes: Additional strategic considerations
    """
    hypothesis: str = Field(..., description="Original research hypothesis")
    complexity: str = Field(..., description="Complexity assessment")
    domains: List[str] = Field(..., description="Relevant knowledge domains")
    research_questions: List[str] = Field(..., description="Questions to investigate")
    agent_assignments: List[Dict[str, Any]] = Field(
        ...,
        description="Work distribution among agents"
    )
    strategy_notes: str = Field(..., description="Strategic considerations")


class ResearchResponse(BaseModel):
    """
    Complete response model for a finished research investigation.

    This comprehensive model contains all results from a multi-agent
    research operation, including the plan, individual agent results,
    synthesis, and final report.

    Attributes:
        task_id: Unique identifier for this research task
        status: Current status of the research
        hypothesis: Original research hypothesis
        plan: Research plan (if available)
        agent_results: Results from each research agent
        synthesis: Synthesized findings across agents
        final_report: Complete formatted research report
        total_duration_seconds: Total execution time
        total_tokens: Total API tokens used
        estimated_cost: Estimated API cost in USD
        created_at: When the task was created
        completed_at: When the task finished (if completed)
    """
    task_id: str = Field(..., description="Unique task identifier")
    status: ResearchStatus = Field(..., description="Task status")
    hypothesis: str = Field(..., description="Research hypothesis")
    plan: Optional[ResearchPlanResponse] = Field(
        None,
        description="Research plan"
    )
    agent_results: List[AgentResultResponse] = Field(
        default_factory=list,
        description="Individual agent results"
    )
    synthesis: str = Field(..., description="Synthesized findings")
    final_report: str = Field(..., description="Complete research report")
    total_duration_seconds: float = Field(
        ...,
        description="Total execution time in seconds"
    )
    total_tokens: int = Field(..., description="Total tokens used")
    estimated_cost: float = Field(..., description="Estimated cost in USD")
    created_at: datetime = Field(..., description="Task creation timestamp")
    completed_at: Optional[datetime] = Field(
        None,
        description="Task completion timestamp"
    )

    class Config:
        """Pydantic model configuration with example."""
        json_schema_extra = {
            "example": {
                "task_id": "abc123",
                "status": "completed",
                "hypothesis": "AI will transform healthcare",
                "final_report": "# Research Report\n\n...",
                "total_duration_seconds": 45.2,
                "total_tokens": 15000,
                "estimated_cost": 0.025
            }
        }


class ResearchStatusResponse(BaseModel):
    """
    Response model for checking async research task status.

    Used to poll the status of background research tasks.

    Attributes:
        task_id: Unique task identifier
        status: Current task status
        progress: Progress percentage (0.0 to 1.0)
        current_stage: Current execution stage name
        agents_completed: Number of agents finished
        agents_total: Total number of agents
        elapsed_seconds: Time since task started
        message: Optional status message or error
    """
    task_id: str = Field(..., description="Task identifier")
    status: ResearchStatus = Field(..., description="Current status")
    progress: float = Field(
        ...,
        ge=0,
        le=1,
        description="Progress from 0 to 1"
    )
    current_stage: str = Field(..., description="Current execution stage")
    agents_completed: int = Field(..., description="Agents finished")
    agents_total: int = Field(..., description="Total agents")
    elapsed_seconds: float = Field(..., description="Elapsed time")
    message: Optional[str] = Field(None, description="Status message")


