# Error Models
# ============================================================================
# Standardized error response models.

class ErrorResponse(BaseModel):
    """
    Standardized error response model.

    Used for all API error responses to provide consistent
    error information to clients.

    Attributes:
        error: Error type or message
        detail: Detailed error description
        code: Machine-readable error code
        timestamp: When the error occurred
    """
    error: str = Field(..., description="Error message")
    detail: Optional[str] = Field(None, description="Detailed description")
    code: Optional[str] = Field(None, description="Error code")
    timestamp: datetime = Field(
        default_factory=datetime.now,
        description="Error timestamp"
    )


class ValidationErrorResponse(BaseModel):
    """
    Validation error response for request validation failures.

    Contains details about which fields failed validation.

    Attributes:
        error: Always "Validation Error"
        details: List of validation error details
    """
    error: str = Field(default="Validation Error", description="Error type")
    details: List[Dict[str, Any]] = Field(..., description="Validation details")
